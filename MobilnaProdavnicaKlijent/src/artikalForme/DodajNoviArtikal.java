    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        Artikal artikal= new Artikal();
        if(validation()){
            if(validationPrice()){
            try {
                
                artikal.setNaziv(txtNaziv.getText());
                artikal.setBoja(txtBoja.getText());
                artikal.setMemorija(txtMemorija.getText());
                artikal.setTipArtikla((TipArtikla) cmbTipArtikla.getSelectedItem());
                artikal.setCena(Double.parseDouble(txtCena.getText()));
                
                
                KlijentskiKontroler.getInstance().sacuvajArtikal(artikal);
                JOptionPane.showMessageDialog(this, "Артикал је креиран!");
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Систем не може да креира артикал.");
            }
            }else{
                JOptionPane.showMessageDialog(this, "Цена мора бити позитиван број");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Поља морају бити попуњена.");
        }
        
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        Artikal artikal= new Artikal();
        if(validation()){
            if(validationPrice()){
            try {
                artikal.setNaziv(txtNaziv.getText());
                artikal.setBoja(txtBoja.getText());
                artikal.setMemorija(txtMemorija.getText());
                artikal.setTipArtikla((TipArtikla) cmbTipArtikla.getSelectedItem());
                artikal.setCena(Double.parseDouble(txtCena.getText()));
                artikal.setSifraArtikla(Integer.parseInt(txtSifra.getText()));
                
                KlijentskiKontroler.getInstance().izmeniArtikal(artikal);
                JOptionPane.showMessageDialog(this, "Артикал је успешно измењен.");
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Систем не може да измени податке о артиклу.");
            }}else{
                 JOptionPane.showMessageDialog(this, "Цена мора бити позитиван број");   
             }}else{
            JOptionPane.showMessageDialog(this, "Поља морају бити попуњена.");
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

