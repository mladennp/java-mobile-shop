    private void srediCmb() {
        JOptionPane.showMessageDialog(this, "Систем је учитао артикал!");
        try {
            cmbTipArtikla.removeAllItems();
            ArrayList<TipArtikla> tipoviArtikala=KlijentskiKontroler.getInstance().vratiTipoveArtikala();
            
            for (TipArtikla tipArtikla : tipoviArtikala) {
                cmbTipArtikla.addItem(tipArtikla);
            }
        } catch (Exception ex) {
            Logger.getLogger(DodajNoviArtikal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    private boolean validation() {
        if(txtBoja.getText().isEmpty() || txtMemorija.getText().isEmpty() || 
                txtCena.getText().isEmpty() || txtNaziv.getText().isEmpty() || cmbTipArtikla.getSelectedItem()==null){
            return false;
        }
        return true;
    }

    private boolean validationPrice() {
        if(Integer.parseInt(txtCena.getText())<0 ){
            return false;
        }
        return true;
    }
